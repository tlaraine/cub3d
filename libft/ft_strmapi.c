#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	int		i;
	int		j;

	j = 0;
	i = 0;
	if (!s || !f)
		return (0);
	while (s[j++])
		;
	if (!(str = malloc(sizeof(char) * j)))
		return (0);
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = 0;
	return (str);
}
